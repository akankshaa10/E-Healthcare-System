  <p>Medicare is the federal health insurance program started in 1965 that is provided for individuals who meet certain criteria. Those who are over the age of 65, younger people who have certain disabilities, or anyone who has End-Stage Renal Disease (ESRD) qualify for Medicare insurance. In 1997 Medicare Advantage (at that time called Medicare+Choice) was signed into law which gave Medicare eligible beneficiaries more options when choosing their health insurance. Medicare has 4 different parts, Part A (Hospital Insurance) and Part B (Medical Insurance) which are the Traditional Medicare, Part C (Medicare Advantage), and Part D (Prescription Drug):<BR>

Part A (Hospital Insurance) – Covers hospital stays, skilled nursing facility care, hospice stays, and some home health care. Part A is free to most Americans who have worked in the U.S. for many years.<BR>

Part B (Medical Insurance) – Covers certain doctors’ services, outpatient care, medical supplies, and preventive services. Beneficiaries must pay a monthly premium for Part B Insurance.<BR>

Part C (Medicare Advantage) – All in one alternative to Parts A, B, and (most times) D. These plans are offered by private insurance companies approved by Medicare. A Medicare Advantage (MA) plan will cover all of traditional Medicare services and may offer extra coverage like dental, hearing, and vision. Generally, one monthly premium covers all Parts A, B, D and additional extra coverage.<BR>

Part D (Prescription Drug) – Provides prescription drug coverage on top of the Traditional Medicare. Part D plans are offered by private companies that are approved by Medicare.<BR>

When a beneficiary is eligible for Medicare, they can choose to enroll in a Part C plan that is offered by a private insurance company. Currently, about 1/3 of Medicare enrollees are in a Part C plan, and this number has been increasing year over year. As MA becomes a bigger piece of the Medicare world, it is important to understand the difference between Traditional Medicare and Medicare Advantage.</p>

